#include <stdio.h>

// Функция для нахождения максимальной длины непрерывной последовательности символов, содержащих искомый символ
int find_max_seq(char str[], char c) {
    int count = 0; // счетчик для подсчета длины текущей непрерывной последовательности
    int max_count = 0; // максимальная длина непрерывной последовательности символов, содержащих искомый символ
    for (int i = 0; i < 15; i++) { // проходим по всему символьному массиву
        if (str[i] == c) { // если текущий символ равен искомому символу
            count++; // увеличиваем счетчик длины
        } else { // если текущий символ не равен искомому символу
            if (count > max_count) { // проверяем, превышает ли длина текущей непрерывной последовательности максимальную длину
                max_count = count; // если да, то обновляем максимальную длину
            }
            count = 0; // обнуляем счетчик длины текущей непрерывной последовательности
        }
    }
    if (count > max_count) { // проверяем длину последней непрерывной последовательности
        max_count = count; // если она больше текущей максимальной длины, обновляем максимальную длину
    }
    return max_count; // возвращаем максимальную длину непрерывной последовательности символов, содержащих искомый символ
}

// Функция для ввода символов от пользователя и вывода результата
int main() {
    char str[15]; // символьный массив размером 15
    char c; // искомый символ
    printf("Enter 15 characters: "); 
    scanf("%s", str); // считываем 15 символов в массив
    printf("Enter a character to search: "); 
    scanf(" %c", &c); // считываем искомый символ 
    int max_seq = find_max_seq(str, c); // вызываем функцию для поиска максимальной длины непрерывной последовательности с заданным символом
    printf("Maximum sequence of %c: %d\n", c, max_seq);
    return 0; 
}
